int	abs(int n)
{
	return ((((n < 0) * -1 * n) + (n * (n >= 0))));
}

unsigned char	get_n_chunks(short n)
{
	return ((((n >= 500) * 8 + (n >= 100 && n < 500) * 4 + (n < 100) * 2)));
}
